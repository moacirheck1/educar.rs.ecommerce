    private void preencheCabecalho(Pedido pedido) {
        double ped = 0;
        if (pedido.getId() == 0) {
            jtxtNome.setText(pedido.getCliente().getNome());
            jtxtData.setText(LocalDate.now().toString());
            for (ItemPedido ip : pedido.getItensPedido()) {
                ped += ip.getValor();
            }
                jtxtValorTotal.setText(ped + "");

        } else {
            jtxtID.setText(pedido.getId() + "");
            jtxtNome.setText(pedido.getCliente().getNome());
            jtxtData.setText(pedido.getData().toString());
            jtxtValorTotal.setText(pedido.getValorTotal() + "");

        }

    }

