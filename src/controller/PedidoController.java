    private void preencheTabela(Pedido pedido) {
        int i = 0;
    
        for (ItemPedido ip : pedido.getItensPedido()) {
            jtTabelaPedido.setValueAt(ip.getProduto().getNome(), i, 0);
            jtTabelaPedido.setValueAt(ip.getQuantidade(), i, 1);
            jtTabelaPedido.setValueAt(ip.getProduto().getPreco(), i, 2);
            jtTabelaPedido.setValueAt(ip.getValor(), i, 3);
          
            i++;
        }
    }


